//-------------------------------------------------------------------------
//定点整数开平方
//-------------------------------------------------------------------------
uint32_t sqrtfix(uint64_t d, uint32_t N)
{
    uint64_t t;
    uint32_t q = 0;

    while (N--) {
        t = (q << 1) + (1 << N);
        if ((d >> N) >= t) {
            d -= (t << N);
            q += (1 << N);
        }
    }
    return q;
}


//-------------------------------------------------------------------------
//无符号整数绝对值
//-------------------------------------------------------------------------
uint_t uintabs(uint_t a, uint_t b)
{

    if (a > b)
        return (a - b);
    else
        return (b - a);
}




